// 44 premium 3000 xp + items
// 45 challenges 1000 xp
// 46 beginner 3000 xp

$.each( BBMissionList, function( key, value ) {
  var miLink = 'assets/missions/'
  var missionNumber = key.split(':');
  missionNumber = missionNumber[1];
  var missionDesc = value.description;
  var missionAction = value.action;
  var missionIcon = miLink+'icon_'+missionAction+'.png';
  console.log(missionNumber);
  var premiumBorder = '';
  var missionTierTitle = '';
  var xpReward = '';
  if(missionNumber.match(/^41/)){
    var xpVal = Math.floor(Math.random() * 3) + 1;
    var xpDaily = {
      1: 1000,
      2: 1500,
      3: 2000
    }
    xpReward = xpDaily[xpVal];
    console.log(xpVal)
    missionTierTitle = `<div class="missionTierTitle">
<span class="freeText">Free</span>
</div>`
  }
  if(missionNumber.match(/^44/)){
    premiumBorder = `<div class="premiumBorder"></div>`;
    missionTierTitle = `<div class="missionTierTitle">
<span class="premiumText">Premium</span>
</div>`
    xpReward = 3000;
  }
  console.log(missionDesc);
  console.log(missionAction);
  var missionBlock = `${missionTierTitle}
<div class="miBlock" data-id="${missionNumber}">
${premiumBorder}
<div class="miIcon"><img src="${missionIcon}"/></div>
<div class="miDesc"><span class"miText">${missionDesc}</span></div>
<div class="miReward"><i class="xp_small"></i><span class="xpValue">${xpReward}</span></div>
</div>`;
  $('.app').append(missionBlock);
});